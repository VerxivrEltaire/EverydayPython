# Sequence comparison

# Comparing lists
list_1 = [1, 2, 3]
list_2 = [1, 2, 4]
result = list_1 < list_2
print(result)  # Output: True

# Comparing tuples
tuple_1 = ('apple', 'banana')
tuple_2 = ('apple', 'orange')
result = tuple_1 < tuple_2
print(result)  # Output: True

# Comparing strings
str_1 = 'hello'
str_2 = 'world'
result = str_1 < str_2
print(result)  # Output: True
